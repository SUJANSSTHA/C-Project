public class RelationOperation {
    public static void main(String []args)
    {
        int q = 5;
        int p = 10;
        System.out.println("p==q\t"+(p==q));
        System.out.println("p!=q\t"+(p!=q));
        System.out.println("p>q\t"+(p>q));
        System.out.println("p>=q\t"+(p>=q));
        System.out.println("p<=q\t"+(p<=q));
    
    }
}
