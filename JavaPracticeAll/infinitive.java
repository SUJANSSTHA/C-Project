// for Each loop 
// for each loop is used to traverse through elements in an array. It is easy to use because we do't have to increment the value It returns the elements form the array or collection one by one.
public class infinitive {
    public static void main (String []args)
    {
        int a[] = {10, 12, 12, 30};
        for(int i:a)
        {
            System.out.println(i);
        }
    }
}